#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	resul;

	i = 0;
	sign = 1;
	resul = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		resul = (resul * 10) + (str[i] - '0');
		i++;
	}
	return (sign * resul);
}
